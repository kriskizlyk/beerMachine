from threading import Timer, Thread, Event

# t = Timer(0.0001, test_target) - Function Call
# t.start() - Start Timer
# t.cancel() - Remove Timer

class TimerEvent():

   def __init__(self,t,hFunction):
      self.t=t
      self.hFunction = hFunction
      self.thread = Timer(self.t,self.handle_function)

   def handle_function(self):
      self.hFunction()
      self.thread = Timer(self.t,self.handle_function)
      self.thread.start()

   def start(self):
      self.thread.start()

   def cancel(self):
      self.thread.cancel()
